// ============================================================
// GameState — 游戏状态枚举
// 职责：定义所有游戏顶层状态，零依赖
// 位置：Core 层基础设施
// 注意：本文件纯枚举，不引入任何依赖，避免循环引用
// ============================================================

export enum GameState {
  /** 引擎启动 / 最小初始化（瞬间过渡） */
  BOOT = 0,

  /** 资源 & 配置加载中 */
  LOADING,

  /** 主菜单 */
  MAIN_MENU,

  /** 战斗中 */
  BATTLE,

  /** 战斗结算 */
  BATTLE_RESULT,

  /** 挂机主界面（核心常驻界面） */
  IDLE,
}
